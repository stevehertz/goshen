                                    <p>We developed the Blue Skies Foundation to work with local communities where Blue
                                        Skies operates.</p>



                                    <p>Our Foundation enables projects to be chosen by farmers living within the
                                        communities. The council of community representatives follow a democratic process
                                        when prioritising projects then take responsibility for creating and evaluating the
                                        project.</p>



                                    <p>To date, the Blue Skies Foundation has invested in more than 120 health and education
                                        projects including erecting school buildings, completing rural sanitation projects,
                                        building fire stations and outfitting health clinics.</p>



                                    <p>These projects have led to increased school attendance and academic performance,
                                        improved community heath and sanitation and greater productivity among farming
                                        communities.</p>



